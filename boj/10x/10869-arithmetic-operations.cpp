#include <iostream>

using namespace std;

int main(){
    
    int a, b;

    cin >> a >> b;

    // addition
    cout << a + b << endl;

    // subtraction
    cout << a - b << endl;

    // multiplication
    cout << a * b << endl;

    // division
    cout << a / b << endl;

    // modulus
    cout << a % b;

    return 0;
}